function Symboll(canvas) {
  this.e = "";
  this.canvas = canvas;
  this.width = "";
  this.height = "";
  this.left = canvas.width/3;
  this.top = canvas.height/3;
  this.fillColor = "";
  this.bordercolor = "";
  this.borderwidth = "";
}

function Text(canvas) {
  this.e = "";
  this.canvas = canvas;
  this.left = canvas.width / 3;
  this.top = canvas.height / 1.5;
  this.width = "";
  this.height = "";
  this.fillColor = "";
  this.fontFamily = "";
}